curso = ' pYtHoN '

print(curso.upper())
# Método upper() retorna a string convertida em maiúsculas
print(curso.lower())
# Método lower() retorna a string convertida em minúsculas
print(curso.title())
# Método title() transforma as primeiras letras de cada palavra
print(curso.swapcase())
# Método swapcase() troca os caracteres maiúsculos por minúsc
print(curso.lstrip())
# Método lstrip() remove espaços à direita da string
print(curso.rstrip())
# Método rstrip() remove espaço à esquerda da string
print(curso.center(10, '#'))
# Método center() centraliza uma string com um caractere especificado
print('.'.join(curso))
# O método join() une todos elementos de uma lista ou tupla separando-os entre